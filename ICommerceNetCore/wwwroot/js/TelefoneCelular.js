function formatarTelefoneCelular(input) {
    // Remove todos os caracteres não numéricos
    const numeroLimpo = input.value.replace(/\D/g, '');

    // Aplica a máscara para telefone celular
    input.value = numeroLimpo.replace(/(\d{2})(\d{5})(\d{4})/, '($1) $2-$3');
}

