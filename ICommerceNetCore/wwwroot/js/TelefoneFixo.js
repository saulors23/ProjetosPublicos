function formatarTelefoneFixo(input) {
    // Remove todos os caracteres não numéricos
    const numeroLimpo = input.value.replace(/\D/g, '');

    // Aplica a máscara para telefone fixo
    input.value = numeroLimpo.replace(/(\d{2})(\d{4})(\d{4})/, '($1) $2-$3');
}

